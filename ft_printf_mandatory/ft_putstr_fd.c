#include "ft_printf.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (!s)
		ft_putstr_fd("(null)", 1);
	else
	{  
		while (s && *s)
		{
			write(fd, s, 1);
			s ++;
		}
	}
}
